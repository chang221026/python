# 04 转义序列
# \" 表示双引号
course_full_name = "Python Programing with \"Mosh\""
print(course_full_name)
# \' 表示单引号
course_full_name = "Python Programing with \'Mosh\'"
print(course_full_name)
# \\ 表示添加反斜杠
course_full_name = "Python Programing with \\Mosh\\"
print(course_full_name)
# \n 表示换行
course_full_name = "Python Programing with \nMosh"
print(course_full_name)

print("ab\"cb")

# 想在字符串中加入引号， 1.单引号包裹双引号'ab"vcd'  2. "ab\"cb" 使用\"