# 07 数字
# Python 中的数字类型
x = 3  # 整数
y = 3.1  # 浮点数
z = 3 + 2j  # 复数 a + bi

print(10 + 3)  # 加法
print(10 - 3)  # 减法
print(10 * 3)  # 乘法
print(10 / 3)  # 除法
print(10 // 3)  # 除法但返回整数
print(10 % 3)  # 取余
print(10 ** 3)  # 幂运算

x = x + 3
print(x)
y += 3
print(y)


