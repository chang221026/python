# Python 中的基本数据类型有哪些？
# 字符串（Strings）
# 整数（Integer number）
# 浮点数（Float numbers）
# 布尔值（Booleans）- True 和 False

# 终端会输出什么？
fruit = "Apple"
print(fruit[1])
# p

# 终端会输出什么？
fruit = "Apple"
# 在切片时，最后一个索引对应的字符不包含在结果中
print(fruit[1:-1])
# ppl

# 结果是什么？
print(10 % 3)
# 1

# 结果是什么？
print(bool("False"))
# True
