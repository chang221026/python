# 08 使用数字
# 要使用更多数学函数，需要导入 math 模块，命令为 import math
import decimal
import math
print(round(2.9595, 2))  # 四舍五入函数
print(abs(-5.4))  # 转换为绝对值
print(math.ceil(8.2))  # 返回大于等于该浮点数的最小整数（向上取整）
print(math.floor(4.9))  # 返回小于等于该浮点数的最大整数（向下取整）

average = 2.9
print(f"{average:.2f}")  # 格式化输出，保留两位小数



